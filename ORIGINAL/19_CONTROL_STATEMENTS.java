// Control Statements -> Tehse are the statements that change the flow of the execution of the statements.

// 1) break -> This statement is used to exit the loop irrespective of the fact that the condition is True or False.

// Example: 

// class CONTROL_STATEMENT
// {
//     public static void main(String[]args)
//     {
//         for (int i = 1;i<=10;i++)
//         {
//             System.out.println(i);
//             if (i==5)
//                 break;      // The execution of the loop will be terminated when the condition is met.
//         }
//     }
// }


// 2) continue -> This statement skips immediate/current execution and moves onto the next one.

// Example: 

// class CONTROL_STATEMENT
// {
//     public static void main(String[]args)
//     {
//         for (int i=1;i<=10;i++)
//         {
//             if (i==5)
//                 continue;            // -> it will the execution when the i=5
//             System.out.println(i);
//         }
//     }
// }

